#ARTHEMATIC OPERATOR
A=10
B=20
#Addition
print("SUM OF", A, "AND", B, "IS", A+B)
#Subtraction
print("DIFFERENCE OF", A ,"AND", B , "IS", A-B)
#Multiplication
print("PRODUCT OF", A, "AND", B, "IS",A*B)
#Division
print("QUOTIENT WHEN",A,"IS DIVIDED BY",B,"IS",A/B)
#Floor Division
print("FLOOR QUOTIENT WHEN",A, "IS DIVIDED BY",B,"IS",A//B)
#Modulo Division
print("REMAINDER WHEN",A,"DIVIEDED",B,"IS",A%B)
#Exponentiation
print("VALUE OF",A,"TO THE POWER",B,"IS",A**B)
